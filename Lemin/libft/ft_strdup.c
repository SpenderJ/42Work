#include <stdlib.h>
#include "libft.h"

char	*ft_strdup(const char *str)
{
	char		*new;
	int			i;

	new = (char *)malloc(sizeof(str));
	i = 0;
	while (str[i] != '\0')
	{
		new[i] = str[i];
		i++;
	}
	new[i] = '\0';
	return (new);

}
