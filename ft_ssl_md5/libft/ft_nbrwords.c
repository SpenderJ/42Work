#include "libft.h"

int	ft_nbrwords(char const *s, char c)
{
	int	i;
	int ac;

	ac = 0;
	i = 0;
	if (s[0] != c)
		ac++;
	while (s[i])
	{
		if (s[i + 1] && s[i] == c && s[i + 1] != c)
			ac++;
		i++;
	}
	return (ac);
}
