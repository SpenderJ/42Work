#include "libft.h"
#include <unistd.h>

void	ft_putstr(char const *s)
{
	if (!s)
		return ;
	write(1, (char *)s, ft_strlen(s));
}

void	ft_putsterr(char const *s)
{
	if (!s)
		return ;
	write(2, (char *)s, ft_strlen(s));
}
