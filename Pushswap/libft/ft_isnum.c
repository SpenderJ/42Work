#include "libft.h"

int		ft_isnum(char *str)
{
	int		i;

	i = -1;
	if (!str)
		return (1);
	while (str[++i] != '\0')
		if ((str[i] < '0' || str[i] > '9') && str[i] != ' ' && str[i] != '+'
				&& str[i] != '-')
			return (0);
	return (1);
}
