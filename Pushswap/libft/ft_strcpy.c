#include "libft.h"

char	*ft_strcpy(char *dest, const char *src)
{
	int	a;

	a = 0;
	while (src[a] != '\0')
	{
		dest[a] = src[a];
		a = a + 1;
	}
	dest[a] = '\0';
	return (dest);
}
