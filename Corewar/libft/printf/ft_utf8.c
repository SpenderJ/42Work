#include "libft.h"

char	*ft_utf8(wint_t nb, char *buff)
{
	if (nb <= 0x007f)
		*buff = nb;
	else if (nb > 0x007f && nb <= 0x07ff)
	{
		*buff++ = ((nb & 1984) >> 6) | 192;
		*buff = (nb & 63) | 128;
	}
	else if (nb > 0x07ff && nb <= 0xffff)
	{
		*buff++ = ((nb & 61440) >> 12) | 224;
		*buff++ = ((nb & 4032) >> 6) | 128;
		*buff = (nb & 63) | 128;
	}
	else if (nb > 0xffff && nb <= 0x0fffff)
	{
		*buff++ = ((nb & 14680064) >> 18) | 240;
		*buff++ = ((nb & 258048) >> 12) | 128;
		*buff++ = ((nb & 4032) >> 6) | 128;
		*buff = (nb & 63) | 128;
	}
	return (++buff);
}
