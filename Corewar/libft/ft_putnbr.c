#include "libft.h"

void	ft_putnbr(int n)
{
	if (n == -2147483648)
	{
		n = 8;
		ft_putstr("-214748364");
	}
	if (n < 0)
	{
		ft_putchar('-');
		n = -n;
	}
	if (n > 9)
	{
		ft_putnbr(n / 10);
		ft_putchar(n % 10 + '0');
	}
	else
		ft_putchar(n + '0');
}
