int		ft_sqrt(int nb)
{
	int	i;
	int	res;

	i = 1;
	res = 0;
	if (nb < 0)
		return (0);
	while (res < nb)
	{
		res = i * i;
		i = i + 1;
	}
	if (res == nb)
		return (i - 1);
	return (0);
}
